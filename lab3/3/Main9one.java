/* Лускина Юлия 

9.	Дана строка, слова в которой состоят из букв латинского алфавита и десятичных цифр. 
Остальные символы считаются разделителями между словами. Получить новую строку, выполняя 
в заданной строке замены по следующему правилу. Все слова, состоящие только из букв, 
записываются строчными буквами. Слова в исходной строке разделяются некоторым множеством 
разделителей. Слова в новой строке должны разделяться ровно одним пробелом. 

Введите строку:
HeLLo JULIA100 likE!
Результат: hello JULIA100 like

Определение разделителей:
Задается строка delimiters, содержащая символы, которые будут считаться 
разделителями между словами (пробелы, табуляции, знаки препинания и др.).
Создание токенизатора:
Создается объект StringTokenizer, который разбивает введенную строку на слова, используя указанные разделители.
Инициализация результата:
Создается объект StringBuffer для накопления результирующей строки.
Обработка каждого слова:
Программа перебирает все токены (слова) из StringTokenizer:
Если слово состоит только из букв (проверяется с помощью регулярного выражения [a-zA-Z]+):
Слово преобразуется в строчные буквы с помощью метода toLowerCase().
Добавляется пробел перед новым словом, если это не первое слово в результирующей строке.
Добавляется слово (возможно, преобразованное) к StringBuffer.
Формирование результирующей строки:
После обработки всех токенов, результирующая строка формируется из содержимого StringBuffer.


*/

import java.io.*;
import java.io.IOException;
import java.util.StringTokenizer;

public class Main9one {
    public static void main(String[] args) {
        // Подготовка для ввода строки с клавиатуры
        InputStreamReader isr = new InputStreamReader(System.in);
        BufferedReader br = new BufferedReader(isr);
        
        System.out.println("Введите строку:");
        String line = "";
        try {
            line = br.readLine();
        } catch (IOException e) {
            System.out.println("Ошибка чтения с клавиатуры");
            return;
        }

        // Определение символов-разделителей
        String delimiters = " \t\n\r,;:!?.()-";
        StringTokenizer st = new StringTokenizer(line, delimiters);
        
        StringBuffer resultBuffer = new StringBuffer(); // Используем StringBuffer для построения результата

        while (st.hasMoreTokens()) {
            String word = st.nextToken();
            // Проверяем, состоит ли слово только из букв
            if (word.matches("[a-zA-Z]+")) {
                // Преобразуем в строчные буквы
                word = word.toLowerCase();
            }
            // Добавляем слово в результирующую строку
            if (resultBuffer.length() > 0) {
                resultBuffer.append(" "); // Добавляем пробел перед новым словом, если это не первое слово
            }
            resultBuffer.append(word);
        }

        // Выводим результирующую строку
        System.out.println("Результат: " + resultBuffer.toString());
    }
}