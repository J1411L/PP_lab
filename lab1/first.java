import java.util.Scanner; // Импортируем класс Scanner для чтения пользовательского ввода

public class first {
    public static void main(String[] args) {
        // Создаем объект Scanner для ввода данных с клавиатуры
        Scanner scanner = new Scanner(System.in);

        // Запрашиваем у пользователя значение x
        System.out.print("Введите значение x: ");
        double x = scanner.nextDouble(); // Читаем значение x как число с плавающей точкой

        // Запрашиваем у пользователя натуральное число k
        System.out.print("Введите натуральное число k: ");
        int k = scanner.nextInt(); // Читаем значение k как целое число

        // Вычисляем параметр ε (эпсилон) на основе введенного k
        // ε = 10^(-k), где k — натуральное число
        double epsilon = Math.pow(10, -k);

        // Инициализация переменной для суммы ряда
        double sum = 0.0;

        // Инициализация первого слагаемого ряда Тейлора для sin(x)
        double term = x; // Первое слагаемое равно x
        int n = 1; // Счетчик для количества слагаемых

        // Цикл для вычисления суммы ряда Тейлора
        // Мы продолжаем добавлять слагаемые, пока модуль текущего слагаемого больше или равен ε
        while (Math.abs(term) >= epsilon) {
            sum += term; // Добавляем текущее слагаемое к сумме

            // Вычисляем следующее слагаемое ряда Тейлора
            // term = (предыдущее слагаемое) * (-x^2) / (2n(2n + 1))
            term *= -x * x / (2 * n * (2 * n + 1)); // Формула для следующего слагаемого
            n++; // Увеличиваем счетчик слагаемых
        }

        // Проверяем случай, если x равно 0
        if (x == 0) {
            sum = 1; // По предельному значению sin(0)/0 = 1
        } else {
            sum /= x; // Нормализуем сумму, делим на x, чтобы получить sin(x)/x
        }

        // Вычисляем стандартное значение sin(x)/x с помощью встроенной функции Java
        double standardValue = Math.sin(x) / x;

        // Выводим результаты на экран с точностью до трех знаков после запятой
        System.out.printf("Приближенное значение sin(x)/x: %.3f%n", sum);
        System.out.printf("Стандартное значение sin(x)/x: %.3f%n", standardValue);

        // Закрываем Scanner, чтобы освободить ресурсы
        scanner.close();
    }
}